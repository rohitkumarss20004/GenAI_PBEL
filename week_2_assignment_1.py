import pandas as pd
import numpy as np

data = {
    "Product_Name": ["TV", "AC", "Tablet", "Phone", "Printer"],
    "Price": [55000, 80000, 24000, 30000, 50000 ]
}

df = pd.DataFrame(data)
df.to_csv("Products_detail.csv")

df = pd.read_csv("Products_detail.csv")
df

Average_price = np.average(df["Price"])
print("Average Price:", Average_price)

def above_average(df, average_price):
  print("Product Above Average Price: ")
  for i in range(len(df)):
      if df["Price"][i] > average_price:
          print(df["Product_Name"][i], "-", df["Price"][i])

above_average(df, Average_price)

