df = pd.read_csv("Temperature_data.csv")
df

highest = np.max(df["Temperature"])
print("Highest Temperature:", highest)
lowest = np.min(df["Temperature"])
print("Lowest Temperature:", lowest)
average = np.mean(df["Temperature"])
print("Average Temperature:", average)

def classify_temp(df):

  for i in range(len(df)):
    temp = df['Temperature'][i]

    if temp < 20:
      category = "Cold"
    elif temp <= 30:
      category = "Moderate"
    else:
      category = "Hot"

    print(df['Day'][i], "-",temp, "-", category)
classify_temp(df)